#Solicite a cotação do dólar atual

cotacao = float(input('Digite a cotação do dólar atual: '))

#Solicite a quantidade de dólares desejada

quantidade = float(input('Digite a quantidade de dólares desejada: '))

#Calcule o valor total em reais que precisará ser pago

valortotal = cotacao * quantidade

print('O valor total em reais que precisará ser pago é:', valortotal)



